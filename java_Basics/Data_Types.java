package java_Basics;

public class Data_Types {

	public static void main(String[] args) {
		
	//primitive data types : int , boolean, char ,double
		
	//1.int
		int a=10;
		System.out.println(a);
		
	//2.double we store int in double but cannot store double is int
		double d=12.12;
		System.out.println(d);
		double d1=100.00; //100
		System.out.println(d1);
		
	//3.char  //it accepts single digit value in single quotes
		char c='a';
		System.out.println(c);
		char c1='1'; //if 1 is int but it's in single quotes
		System.out.println(c1);
		char c2='$';
		System.out.println(c2);

	//4.boolean
		boolean b1=true; 
		System.out.println(b1);
		boolean b2=false;
		System.out.println(b2);
		
	//5.string :is a class , not a data type 
		String s="hello world";
		System.out.println(s);
		String s1="selenium";
		System.out.println(s1);
		String s2="198";
		System.out.println(s2);
		String s3="12.32";
		System.out.println(s3);
		
	
		
		
		
		
	}

}
